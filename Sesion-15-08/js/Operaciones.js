
function Sumar(){
    let numero1;
    let  numero2;
    let resultado;

    numero1 = parseFloat(prompt("Digite el primer número: "));
    numero2 = parseFloat(prompt("Digite el segundo número: "));

    resultado = numero1+numero2;

    alert("La suma entre "+numero1+" y "+numero2+" es: "+resultado);
}


